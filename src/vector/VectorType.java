package vector;

/**
 *  This enum is used to differentiate the vectors.
 */
public enum VectorType
{
    /**
     *  An ellipse vector.
     * */
    ELLIPSE,

    /**
     *  A rectangle vector.
     * */
    RECTANGLE,

    /**
     *  A text vector.
     * */
    TEXT,

    /**
     *  An undefined vector.
     * */
    UNDEFINED
}
